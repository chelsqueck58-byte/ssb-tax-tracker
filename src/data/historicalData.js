export function getHistoricalData(ticker, announcementDate, implementationDate) {
  const key = `${ticker}|${announcementDate}`
  const entry = REAL_PRICES[key]
  if (!entry) return null

  const series = entry.data.map(([date, price]) => ({ date, price }))

  // Inject exact event dates so ReferenceLine can match them
  const eventDates = [
    { date: announcementDate, event: 'announcement' },
    { date: implementationDate, event: 'implementation' },
  ]

  for (const ev of eventDates) {
    const evTime = new Date(ev.date).getTime()
    if (series.some(p => p.date === ev.date)) continue

    let insertIdx = series.findIndex(p => new Date(p.date).getTime() > evTime)
    if (insertIdx === -1 || insertIdx === 0) continue

    // Use real daily close if available, otherwise interpolate
    let price
    const daily = DAILY_PRICES[key]
    const dailyMatch = daily && daily.find(([d]) => d === ev.date)
    if (dailyMatch) {
      price = dailyMatch[1]
    } else {
      const before = series[insertIdx - 1]
      const after = series[insertIdx]
      const t1 = new Date(before.date).getTime()
      const t2 = new Date(after.date).getTime()
      const ratio = (evTime - t1) / (t2 - t1)
      price = Math.round((before.price + ratio * (after.price - before.price)) * 100) / 100
    }

    series.splice(insertIdx, 0, { date: ev.date, price })
  }

  return { series, unit: entry.unit }
}

export function getAnnouncementReaction(ticker, announcementDate, implementationDate) {
  const key = `${ticker}|${announcementDate}`

  // 1D%: use daily data (day before → day of announcement)
  let annDay = null
  const daily = DAILY_PRICES[key]
  if (daily && daily.length >= 2) {
    const priceBefore = daily[0][1]
    const priceAnn = daily[1][1]
    annDay = Math.round(((priceAnn - priceBefore) / priceBefore) * 1000) / 10
  }

  // 1W%: use weekly data (week before → week after announcement)
  let annWeek = null
  const result = getHistoricalData(ticker, announcementDate, implementationDate)
  if (result) {
    const { series } = result
    const annTime = new Date(announcementDate).getTime()
    let annIdx = -1
    for (let i = 0; i < series.length; i++) {
      if (series[i].date === announcementDate) { annIdx = i; break }
      if (new Date(series[i].date).getTime() > annTime) { annIdx = i; break }
    }
    if (annIdx >= 1) {
      const priceBefore = series[annIdx - 1].price
      const oneWeekMs = 7 * 24 * 60 * 60 * 1000
      const targetTime = annTime + oneWeekMs
      let weekIdx = annIdx
      for (let i = annIdx + 1; i < series.length; i++) {
        if (new Date(series[i].date).getTime() >= targetTime) { weekIdx = i; break }
      }
      if (weekIdx === annIdx && annIdx + 1 < series.length) weekIdx = annIdx + 1
      const priceWeek = series[weekIdx].price
      annWeek = Math.round(((priceWeek - priceBefore) / priceBefore) * 1000) / 10
    }
  }

  return { annDay, annWeek }
}

// Seeded PRNG for sparkline (decorative only)
function seededRandom(seed) {
  let s = seed
  return () => {
    s = (s * 16807 + 0) % 2147483647
    return (s - 1) / 2147483646
  }
}

function hashTicker(ticker) {
  let hash = 0
  for (let i = 0; i < ticker.length; i++) {
    hash = ((hash << 5) - hash) + ticker.charCodeAt(i)
    hash |= 0
  }
  return Math.abs(hash)
}

export function getSparklineData(ticker) {
  const rand = seededRandom(hashTicker(ticker) + 999)
  const points = [100]
  for (let i = 1; i < 30; i++) {
    const vol = 0.015
    const drift = (rand() - 0.48) * 0.01
    const shock = (rand() - 0.5) * 2 * vol
    points.push(Math.max(points[i - 1] * (1 + drift + shock), 50))
  }
  return points
}
